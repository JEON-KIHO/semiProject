package product;

public class ProductVO {
	private int rn;
	private String code;
	private String pname;
	private int price;
	private String image;
	
	public int getRn() {
		return rn;
	}
	public void setRn(int rn) {
		this.rn = rn;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
	@Override
	public String toString() {
		return "ProductVO [rn=" + rn + ", code=" + code + ", pname=" + pname + ", price=" + price + ", image=" + image
				+ "]";
	}
	
}
