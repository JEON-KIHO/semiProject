package account;

public class AccountVO {
	
	private String id;
	private String pw;
	private int code;
	private String name;
	private String nickName;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	
	@Override
	public String toString() {
		return "AccountVO [id=" + id + ", pw=" + pw + ", code=" + code + ", name=" + name + ", nickName=" + nickName
				+ "]";
	}
}
